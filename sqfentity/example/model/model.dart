    _addedBlocks.waitingStartBlock.add(true);
    _addedBlocks.needEndBlock.add(false);
    _blockIndex++;
    if (_blockIndex > 1) 
    {_addedBlocks.needEndBlock[_blockIndex - 1] = true;}
